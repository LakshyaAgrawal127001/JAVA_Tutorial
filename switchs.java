import java.util.Scanner;

public class switchs {
    public static void main(String[] args) {
        
        int day = 1;

        switch (day) {
            case 1:
                System.out.println("Tuesday");
                break;
            case 2:
                System.out.println("mon- sat");
            default:
                System.out.println("NO days");
                break;
        }

     }
}
